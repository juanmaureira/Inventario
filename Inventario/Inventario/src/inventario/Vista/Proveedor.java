    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        nombre=txtNombre.getText();
        rut=txtRut.getText() + txtDv.getText();
        direccion=txtDireccion.getText();
        telefono=cbTelefono.getSelectedItem() + txtTelefono.getText();
        web = txtWeb.getText();
        
        
        if( nombre.isEmpty() || rut.isEmpty() || direccion.isEmpty() || telefono.isEmpty() || web.isEmpty() ){
            Warning warning = new Warning(new javax.swing.JDialog(),true);
            warning.setVisible(true);
            
        }else{
            proveedorClass proveedor = new proveedorClass(rut, nombre, direccion, telefono, web);
            o[0] = proveedor.getNombre();
            o[1] = proveedor.getRut();
            o[2] = proveedor.getDireccion();
            o[3] = proveedor.getTelefono();
            o[4] = proveedor.getWeb();
            
            dtm.addRow(o);
            
            txtNombre.setText(null);
            txtRut.setText(null);
            txtDv.setText(null);
            txtDireccion.setText(null);
            txtTelefono.setText(null);
            txtWeb.setText(null);
        }        
    }//GEN-LAST:event_jButton1ActionPerformed

